import streamlit as st
import requests
import datetime
from utils.pdf_generator import generate_pdf_from_text


# from exception.exceptions import TradingBotException
import sys

BASE_URL = "http://localhost:8000"  # Backend endpoint

st.set_page_config(
    page_title="üåç Travel Planner Agentic Application",
    layout="centered",
    initial_sidebar_state="expanded",
)

st.title("üåç Travel Planner Agentic Application")

# Initialize chat history
if "messages" not in st.session_state:
    st.session_state.messages = []

# Display chat history
st.header("How can I help you in planning a trip? Let me know where do you want to visit.")

# Chat input box at bottom
with st.form(key="query_form", clear_on_submit=True):
    user_input = st.text_input("User Input", placeholder="e.g. Plan a trip to Goa for 5 days")
    submit_button = st.form_submit_button("Send")

if submit_button and user_input.strip():
    try:
        # # Show user message
        # Show thinking spinner while backend processes
        with st.spinner("Bot is thinking..."):
            payload = {"question": user_input}
            response = requests.post(f"{BASE_URL}/query", json=payload)

        if response.status_code == 200:
            answer = response.json().get("answer", "No answer returned.")
            markdown_content = f"""# üåç AI Travel Plan

            # **Generated:** {datetime.datetime.now().strftime('%Y-%m-%d at %H:%M')}  
            # **Created by:** Abhishek's Travel Agent

            ---

            {answer}

            ---

            *This travel plan was generated by AI.
            Please verify all information, especially prices, operating hours, 
            and travel requirements before your trip.*
            """
            st.markdown(markdown_content)
            
            
            #Generate PDF
            pdf_file = generate_pdf_from_text(answer)

            # Add download button
            st.download_button(
                label="üì• Download Travel Plan as PDF",
                data=pdf_file,
                file_name="travel_plan.pdf",
                mime="application/pdf"
            )

            
        else:
            st.error(" Bot failed to respond: " + response.text)

    except Exception as e:
        st.error(f"‚ùå Error: {str(e)}")
        st.stop()